import sys
import re
import subprocess
from pathlib import Path
from datetime import datetime, timedelta

PROJECT_ROOT = Path(__file__).parent.parent
SPRINTS_DIR = PROJECT_ROOT / "docs" / "sprints"
STANDUPS_DIR = SPRINTS_DIR / "standups"
REVIEWS_DIR = SPRINTS_DIR / "reviews"
RETROS_DIR = SPRINTS_DIR / "retros"
BACKLOG_FILE = SPRINTS_DIR / "backlog.md"
VELOCITY_FILE = SPRINTS_DIR / "velocity.md"
