# Ensure dirs exist
for d in [STANDUPS_DIR, REVIEWS_DIR, RETROS_DIR]:
    d.mkdir(parents=True, exist_ok=True)


# ANSI colors
class C:
    RED = "\033[91m"
    GREEN = "\033[92m"
    YELLOW = "\033[93m"
    BLUE = "\033[94m"
    MAGENTA = "\033[95m"
    CYAN = "\033[96m"
    BOLD = "\033[1m"
    DIM = "\033[2m"
    RESET = "\033[0m"


def parse_tracker():
    """Parse sprint-tracker.yaml."""
    try:
        import yaml

        with open(TRACKER_FILE) as f:
            return yaml.safe_load(f.read())
    except ImportError:
        print(
            f"{C.YELLOW}Warning: PyYAML not installed. Install with: pip install pyyaml{C.RESET}"
        )
        return {}
    except FileNotFoundError:
        print(f"{C.RED}Error: {TRACKER_FILE} not found{C.RESET}")
        return {}


def get_current_sprint():
    """Get current sprint number."""
    data = parse_tracker()
    return data.get("current_sprint", 1)


def get_test_count():
    """Run pytest --co to count tests."""
    try:
        result = subprocess.run(
            ["python", "-m", "pytest", "tests/", "--co", "-q"],
            capture_output=True,
            text=True,
            timeout=30,
            cwd=str(PROJECT_ROOT),
        )
        match = re.search(r"(\d+) tests? selected", result.output)
        if match:
            return int(match.group(1))
    except Exception:
        pass
    return "?"


