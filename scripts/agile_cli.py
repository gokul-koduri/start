# ── Main ────────────────────────────────────────────────
def main():
    args = sys.argv[1:]

    if not args or args[0] in ("-h", "--help"):
        cmd_help()
        return

    command = args[0]

    if command == "planning":
        cmd_planning()
    elif command == "standup":
        ci_mode = "--ci" in args
        cmd_standup(ci_mode=ci_mode)
    elif command == "review":
        sprint = None
        if "--sprint" in args:
            try:
                idx = args.index("--sprint")
                sprint = int(args[idx + 1])
            except (IndexError, ValueError):
                pass
        cmd_review(sprint)
    elif command == "retrospective":
        cmd_retrospective()
    elif command == "backlog":
        cmd_backlog()
    elif command == "metrics":
        cmd_metrics()
    elif command == "create-task":
        cmd_create_task()
    elif command == "move-task":
        if len(args) < 3:
            print(f"{C.RED}Usage: agile_cli.py move-task T-001 done{C.RESET}")
            return
        cmd_move_task(args[1], args[2])
    elif command == "burndown":
        cmd_burndown()
    elif command == "labels":
        cmd_labels()
    else:
        print(f"{C.RED}Unknown command: {command}{C.RESET}")
        cmd_help()


if __name__ == "__main__":
    main()
