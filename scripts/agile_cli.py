# ── Backlog ─────────────────────────────────────────────
def cmd_backlog():
    """Show product backlog."""
    if BACKLOG_FILE.exists():
        print(BACKLOG_FILE.read_text())
    else:
        # Generate from tracker
        data = parse_tracker()
        sprints = data.get("sprints", {})

        backlog = f"""# 📋 Product Backlog

> Auto-generated from sprint-tracker.yaml on {datetime.now().strftime('%Y-%m-%d')}

## Summary

"""
        for i in range(1, 9):
            sprint_key = f"sprint_{i}"
            sprint = sprints.get(sprint_key, {})
            name = sprint.get("name", f"Sprint {i}")
            status = sprint.get("status", "NOT STARTED")
            pct = sprint.get("completion_pct", 0)
            tasks = sprint.get("tasks", {})
            sum(1 for t in tasks.values() if t.get("status") == "DONE")
            len(tasks)

            status_icon = {
                "COMPLETE": "✅",
                "BLOCKED": "🔒",
                "IN_PROGRESS": "⏳",
            }.get(status, "🔲")

            backlog += f"### {status_icon} Sprint {i}: {name} ({status} — {pct}%)\n"
            if tasks:
                for tid, t in sorted(tasks.items()):
                    icon = "✅" if t.get("status") == "DONE" else "🔲"
                    backlog += f"- {icon} {tid}: {t.get('description', '')} ({t.get('effort', '?')})\n"
            backlog += "\n"

        BACKLOG_FILE.write_text(backlog)
        print(backlog)


# ── Metrics / Velocity ─────────────────────────────────
def cmd_metrics():
    """Show velocity and metrics."""
    data = parse_tracker()
    sprints = data.get("sprints", {})
    current = data.get("current_sprint", 1)

    print(f"\n{C.BOLD}{'='*60}{C.RESET}")
    print(f"{C.BOLD}  📊 VELOCITY & METRICS{C.RESET}")
    print(f"{'='*60}{C.RESET}\n")

    print(f"  {'SPRINT':<10} {'STATUS':<15} {'TASKS':<15} {'COMPLETION'}")
    print(f"  {'─'*10} {'─'*15} {'─'*15} {'─'*12}")

    total_done = 0
    total_tasks = 0

    for i in range(1, 9):
        sprint_key = f"sprint_{i}"
        sprint = sprints.get(sprint_key, {})
        tasks = sprint.get("tasks", {})
        done = sum(1 for t in tasks.values() if t.get("status") == "DONE")
        total = len(tasks)
        total_done += done
        total_tasks += total
        status = sprint.get("status", "NOT STARTED")
        pct = sprint.get("completion_pct", 0)

        marker = " ◀" if i == current else ""
        status_color = {
            "COMPLETE": C.GREEN,
            "BLOCKED": C.RED,
            "IN_PROGRESS": C.YELLOW,
        }.get(status, C.DIM)

        print(
            f"  {i:<10} {status_color}{status:<15}{C.RESET} {done}/{total:<13} {pct}%{marker}"
        )

    print(
        f"\n  {C.BOLD}Total: {total_done}/{total_tasks} tasks across 8 sprints{C.RESET}"
    )

    # Test count
    test_count = get_test_count()
    print(f"  {C.BOLD}Tests: {test_count}{C.RESET}")

    print()


# ── Create Task ─────────────────────────────────────────
def cmd_create_task():
    """Interactive task creation."""
    sprint = get_current_sprint()
    data = parse_tracker()
    sprint_key = f"sprint_{sprint}"
    sprint_data = data.get("sprints", {}).get(sprint_key, {})
    tasks = sprint_data.get("tasks", {})

    # Find next task ID
    existing_ids = []
    for tid in tasks.keys():
        match = re.match(r"T-(\d+)", tid)
        if match:
            existing_ids.append(int(match.group(1)))

    next_id = max(existing_ids, default=0) + 1
    task_id = f"T-{next_id:03d}"

    print(f"\n{C.BOLD}Creating task {task_id} for Sprint {sprint}{C.RESET}\n")

    description = input("Description: ").strip()
    if not description:
        print(f"{C.RED}Cancelled.{C.RESET}")
        return

    effort = input("Effort estimate (e.g. 2h, 3 points): ").strip() or "TBD"
    priority = input("Priority (P0/P1/P2): ").strip() or "P1"

    # Build task entry
    task_entry = f"""  {task_id}: {{ description: "{description}", effort: "{effort}", status: "TODO", priority: "{priority}", created_at: "{datetime.now().isoformat()}" }}"""

    print(f"\n{C.BOLD}Task to add to sprint-tracker.yaml:{C.RESET}")
    print(task_entry)
    print(
        f"\n{C.DIM}Add this to the tasks section of sprint_{sprint} in {TRACKER_FILE}{C.RESET}"
    )

    # Also create a GitHub issue body suggestion
    issue_body = f"""## Task: {task_id}

**Description**: {description}
**Sprint**: {sprint}
**Priority**: {priority}
**Effort**: {effort}

### Acceptance Criteria
- [ ]

### Implementation Notes
<!-- Add technical details -->
"""
    print(f"\n{C.BOLD}GitHub Issue Body:{C.RESET}")
    print(issue_body)


# ── Move Task ───────────────────────────────────────────
def cmd_move_task(task_id, new_status):
    """Move a task to a new status column."""
    data = parse_tracker()
    sprint = get_current_sprint()
    sprint_key = f"sprint_{sprint}"
    sprint_data = data.get("sprints", {}).get(sprint_key, {})
    tasks = sprint_data.get("tasks", {})

    if task_id not in tasks:
        print(f"{C.RED}Task {task_id} not found in Sprint {sprint}{C.RESET}")
        return

    valid_statuses = ["TODO", "IN_PROGRESS", "IN_REVIEW", "DONE", "BLOCKED"]
    new_status = new_status.upper()
    if new_status not in valid_statuses:
        print(
            f"{C.RED}Invalid status: {new_status}. Use: {', '.join(valid_statuses)}{C.RESET}"
        )
        return

    old_status = tasks[task_id].get("status", "TODO")
    tasks[task_id]["status"] = new_status

    if new_status == "DONE":
        tasks[task_id]["completed_at"] = datetime.now().isoformat()

    # Write back
    try:
        import yaml

        data["sprints"][sprint_key]["tasks"][task_id] = tasks[task_id]
        with open(TRACKER_FILE, "w") as f:
            yaml.dump(
                data, f, default_flow_style=False, allow_unicode=True, sort_keys=False
            )
        print(f"{C.GREEN}✅ {task_id}: {old_status} → {new_status}{C.RESET}")
    except ImportError:
        print(f"{C.YELLOW}PyYAML required. Install: pip install pyyaml{C.RESET}")
        print(
            f"{C.DIM}Manually update {task_id} status to {new_status} in {TRACKER_FILE}{C.RESET}"
        )


# ── Burndown ────────────────────────────────────────────
def cmd_burndown():
    """Show ASCII burndown chart."""
    sprint = get_current_sprint()
    data = parse_tracker()
    sprint_key = f"sprint_{sprint}"
    sprint_data = data.get("sprints", {}).get(sprint_key, {})
    tasks = sprint_data.get("tasks", {})
    total = len(tasks)
    done = sum(1 for t in tasks.values() if t.get("status") == "DONE")
    remaining = total - done

    print(f"\n{C.BOLD}  📉 Sprint {sprint} Burndown{C.RESET}")
    print(f"  Total: {total} tasks | Done: {done} | Remaining: {remaining}\n")

    # Simple ASCII bar chart
    ideal_per_day = total / 14  # 14-day sprint

    print("  Day  Remaining  Chart")
    print(f"  {'─'*3}  {'─'*9}  {'─'*30}")

    # Show ideal line and actual
    for day in range(0, 15, 2):
        ideal = max(0, total - (ideal_per_day * day))
        bar_len = int(ideal / total * 30) if total > 0 else 0
        bar = f"{C.GREEN}{'█' * bar_len}{C.RESET}{C.DIM}{'░' * (30 - bar_len)}{C.RESET}"
        print(f"  {day:>3}  {int(ideal):>9}  {bar}")

    print(f"\n  {C.BOLD}Current: {remaining} remaining (Day ~?){C.RESET}")
    print()


