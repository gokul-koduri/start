# ── Daily Standup ───────────────────────────────────────
def cmd_standup(ci_mode=False):
    """Generate daily standup template."""
    today = datetime.now().strftime("%Y-%m-%d")
    sprint = get_current_sprint()
    data = parse_tracker()
    sprint_key = f"sprint_{sprint}"
    sprint_data = data.get("sprints", {}).get(sprint_key, {})
    tasks = sprint_data.get("tasks", {})

    # Categorize tasks
    done_tasks = [(tid, t) for tid, t in tasks.items() if t.get("status") == "DONE"]
    pending_tasks = [(tid, t) for tid, t in tasks.items() if t.get("status") != "DONE"]
    total = len(tasks)
    done_count = len(done_tasks)
    pct = int(done_count / total * 100) if total > 0 else 0

    # Check for recently completed (last 24h)
    yesterday_tasks = []
    for tid, t in done_tasks:
        completed = t.get("completed_at", "")
        if completed:
            try:
                completed_date = datetime.fromisoformat(
                    completed.replace("Z", "+00:00")
                ).date()
                if (datetime.now().date() - completed_date).days <= 1:
                    yesterday_tasks.append(f"  ✅ {tid}: {t.get('description', '')}")
            except (ValueError, TypeError):
                pass

    template = f"""# 🏃 Daily Standup — {today}

## Sprint {sprint}: {sprint_data.get('name', 'TBD')}
Progress: {done_count}/{total} tasks ({pct}%)

### What did I complete yesterday?
{chr(10).join(yesterday_tasks) if yesterday_tasks else "  _(No tasks completed yesterday)_"}

### What am I working on today?
- [ ]

### Any blockers?
-

### Quick Metrics
- Sprint progress: {pct}%
- Tasks remaining: {total - done_count}
- Sprint end: _(calculate from planning date)_

---
*Generated by agile_cli.py — {datetime.now().strftime('%H:%M:%S')}*
"""

    output_file = STANDUPS_DIR / f"{today}.md"
    output_file.write_text(template)

    if ci_mode:
        # CI mode: print summary for GitHub Actions
        print(f"Sprint {sprint}: {done_count}/{total} tasks ({pct}%)")
        if yesterday_tasks:
            print(f"Recently completed: {len(yesterday_tasks)} tasks")
        if pending_tasks:
            print(f"Remaining: {len(pending_tasks)} tasks")
    else:
        print(f"{C.GREEN}✅ Standup created: {output_file}{C.RESET}")
        print(f"\n{C.BOLD}Sprint {sprint} Status:{C.RESET}")
        print(f"  Progress: {done_count}/{total} ({pct}%)")

        # Progress bar
        bar_len = 30
        filled = int(bar_len * pct / 100)
        bar = f"{C.GREEN}{'█' * filled}{C.DIM}{'░' * (bar_len - filled)}{C.RESET}"
        print(f"  [{bar}] {pct}%")

        if yesterday_tasks:
            print(f"\n  {C.BOLD}Recently completed:{C.RESET}")
            for t in yesterday_tasks:
                print(f"    {t}")

        if pending_tasks[:5]:
            print(f"\n  {C.BOLD}Up next:{C.RESET}")
            for tid, t in pending_tasks[:5]:
                print(
                    f"    🔲 {tid}: {t.get('description', '')} ({t.get('effort', '?')})"
                )

        print(
            f"\n{C.DIM}Edit the standup file, then close the issue when done.{C.RESET}"
        )


# ── Sprint Review ───────────────────────────────────────
def cmd_review(sprint_num=None):
    """Generate sprint review report."""
    sprint = sprint_num or get_current_sprint()
    data = parse_tracker()
    sprint_key = f"sprint_{sprint}"
    sprint_data = data.get("sprints", {}).get(sprint_key, {})

    if not sprint_data:
        print(f"{C.RED}No data found for Sprint {sprint}{C.RESET}")
        return

    tasks = sprint_data.get("tasks", {})
    objectives = sprint_data.get("objectives", {})
    deliverables = sprint_data.get("deliverables", {})
    sprint_data.get("acceptance_criteria", {})
    metrics = sprint_data.get("metrics", {})
    dod = sprint_data.get("definition_of_done", {})

    done_tasks = sum(1 for t in tasks.values() if t.get("status") == "DONE")
    total_tasks = len(tasks)

    report = f"""# 📊 Sprint {sprint} Review — {datetime.now().strftime('%Y-%m-%d')}

## Sprint: {sprint_data.get('name', 'TBD')}
## Theme: {sprint_data.get('theme', 'TBD')}

---

## Summary

| Metric | Count |
|---|---|
| Total Tasks | {total_tasks} |
| Completed | {done_tasks} |
| Remaining | {total_tasks - done_tasks} |
| Completion | {sprint_data.get('completion_pct', 0)}% |

## ✅ Completed Tasks

"""
    for tid, t in sorted(tasks.items()):
        if t.get("status") == "DONE":
            report += (
                f"- ✅ {tid}: {t.get('description', '')} ({t.get('effort', '?')})\n"
            )

    report += """
## 🔲 Incomplete Tasks

"""
    for tid, t in sorted(tasks.items()):
        if t.get("status") != "DONE":
            report += f"- 🔲 {tid}: {t.get('description', '')} ({t.get('effort', '?')}) — {t.get('status', 'TODO')}\n"

    report += """
## Objectives

"""
    for oid, o in objectives.items():
        icon = "✅" if o.get("status") in ("DONE", "MET") else "🔲"
        report += (
            f"- {icon} {oid}: {o.get('description', '')} — {o.get('status', 'TODO')}\n"
        )

    report += """
## Deliverables

"""
    for did, d in deliverables.items():
        icon = "✅" if d.get("status") == "DONE" else "🔲"
        report += (
            f"- {icon} {did}: {d.get('artifact', '')} — {d.get('status', 'TODO')}\n"
        )

    report += """
## Metrics

| Metric | Target | Actual | Met? |
|---|---|---|---|
"""
    for mid, m in metrics.items():
        met = "✅" if m.get("met", False) else "❌"
        report += (
            f"| {mid} | {m.get('target', '?')} | {m.get('actual', '?')} | {met} |\n"
        )

    report += """
## Definition of Done

"""
    for key, val in dod.items():
        icon = "✅" if val else "❌"
        label = key.replace("_", " ").title()
        report += f"- {icon} {label}\n"

    report += f"""
---

## Demo Items
<!-- List features to demo -->

## Feedback
<!-- Collect feedback from stakeholders -->

## Action Items
<!-- Carry forward to next sprint -->

---
*Generated by agile_cli.py on {datetime.now().isoformat()}*
"""

    output_file = (
        REVIEWS_DIR / f"sprint-{sprint}-review-{datetime.now().strftime('%Y-%m-%d')}.md"
    )
    output_file.write_text(report)
    print(f"{C.GREEN}✅ Sprint review created: {output_file}{C.RESET}")


